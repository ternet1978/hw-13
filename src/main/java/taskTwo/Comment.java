package taskTwo;

class Comment {
    private Long postId;
    private Long id;
    private String name;
    private String email;
    private String body;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public Long getPostId() {
        return postId;
    }

    public String getBody() {
        return body;
    }

    public Long getId() {
        return id;
    }

}
